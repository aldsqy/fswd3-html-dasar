// contoh code structure dimana terdapat perintah untuk menjalankan aksi, dan semicolon untuk penanda akhir
// lalu di lanjut dengan pendeklarasian var, let dan const

//Pendkelarasian Var
var x = "Aldi Arya WInata";
console.log(x); //Output: Aldi Arya Winata
console.log(typeof x); //Output: String


//Pendkelarasian Let 
let a = "Universitas Gunadarma";
console.log(a); //Output: Aldi Arya Winata
console.log(typeof a); //Output: String


//Pendkelarasian Const
const e = 10120095;
console.log(e); //Output: 10120095
console.log(typeof e); //Output: Number